<script>
	import wPicker from "@/components/w-picker/w-picker.vue";
    import xiaEditor from '@/components/xia-editor/xia-editor';
    import multipleSelect from "@/components/momo-multipleSelect/momo-multipleSelect";
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				ceshimingcheng: '',
				ceshileibie: '',
				fengmian: '',
				jiaoshigonghao: '',
				jiaoshixingming: '',
				xuehao: '',
				xingming: '',
				cepingchengji: '',
				pingcebaogao: '',
				jiankangzhuangtai: '',
				jiaoshipingyu: '',
				dengjiriqi: '',
				userid: '',
				},
				jiankangzhuangtaiOptions: [],
				jiankangzhuangtaiIndex: 0,
				// 登录用户信息
				user: {},
                ro:{
                   ceshimingcheng : false,
                   ceshileibie : false,
                   fengmian : false,
                   jiaoshigonghao : false,
                   jiaoshixingming : false,
                   xuehao : false,
                   xingming : false,
                   cepingchengji : false,
                   pingcebaogao : false,
                   jiankangzhuangtai : false,
                   jiaoshipingyu : false,
                   dengjiriqi : false,
                   userid : false,
                },
			}
		},
		components: {
			wPicker,
            xiaEditor,
            multipleSelect,
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
            this.ruleForm.dengjiriqi = this.$utils.getCurDate();
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.jiaoshigonghao = this.user.jiaoshigonghao
			this.ro.jiaoshigonghao = true;
			this.ruleForm.jiaoshixingming = this.user.jiaoshixingming
			this.ro.jiaoshixingming = true;


			// 自定义下拉框值
			this.jiankangzhuangtaiOptions = "健康,异常".split(',')

			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("appUserid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`pingcebaogao`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='ceshimingcheng'){
					this.ruleForm.ceshimingcheng = obj[o];
					this.ro.ceshimingcheng = true;
					continue;
					}
					if(o=='ceshileibie'){
					this.ruleForm.ceshileibie = obj[o];
					this.ro.ceshileibie = true;
					continue;
					}
					if(o=='fengmian'){
					this.ruleForm.fengmian = obj[o].split(",")[0];
					this.ro.fengmian = true;
					continue;
					}
					if(o=='jiaoshigonghao'){
					this.ruleForm.jiaoshigonghao = obj[o];
					this.ro.jiaoshigonghao = true;
					continue;
					}
					if(o=='jiaoshixingming'){
					this.ruleForm.jiaoshixingming = obj[o];
					this.ro.jiaoshixingming = true;
					continue;
					}
					if(o=='xuehao'){
					this.ruleForm.xuehao = obj[o];
					this.ro.xuehao = true;
					continue;
					}
					if(o=='xingming'){
					this.ruleForm.xingming = obj[o];
					this.ro.xingming = true;
					continue;
					}
					if(o=='cepingchengji'){
					this.ruleForm.cepingchengji = obj[o];
					this.ro.cepingchengji = true;
					continue;
					}
					if(o=='pingcebaogao'){
					this.ruleForm.pingcebaogao = obj[o];
					this.ro.pingcebaogao = true;
					continue;
					}
					if(o=='jiankangzhuangtai'){
					this.ruleForm.jiankangzhuangtai = obj[o];
					this.ro.jiankangzhuangtai = true;
					continue;
					}
					if(o=='jiaoshipingyu'){
					this.ruleForm.jiaoshipingyu = obj[o];
					this.ro.jiaoshipingyu = true;
					continue;
					}
					if(o=='dengjiriqi'){
					this.ruleForm.dengjiriqi = obj[o];
					this.ro.dengjiriqi = true;
					continue;
					}
					if(o=='userid'){
					this.ruleForm.userid = obj[o];
					this.ro.userid = true;
					continue;
					}
